    private String convertMonth(String month) {
    	switch (month) {
    	case "JANUARY":
    		return "01";
    	case "FEBRUARY":
    		return "02";
    	case "MARCH":
    		return "03";
    	case "APRIL":
    		return "04";
    	case "MAY":
    		return "05";
    	case "JUNE":
    		return "06";
    	case "JULY":
    		return "07";
    	case "AUGUST":
    		return "08";
    	case "SEPTEMBER":
    		return "09";
    	case "OCTOBER":
    		return "10";
    	case "NOVEMBER":
    		return "11";
    	case "DECEMBER":
    		return "12";
    	}
		return " ";
    }
    
    private int countBlank(String[] arr) {
    	int count = 0;
    	for (int i =0;i<=3;i++) {
    		if (arr[i].isBlank())
    			count +=1;
    	}
    	return count;
    }
    private int countNull(String[] arr) {
    	int count = 0;
    	for (int i =0;i<=3;i++) {
    		if (arr[i]== null)
    			count +=1;
    	}
    	return count;
    }
    

